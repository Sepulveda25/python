
print ("The is a backslash (\\) mark.")
#out: The is a backslash (\) mark. 

print ("The is tab \t key.")
#out: The is tab       key. 

print ("The are \'single quotes\'")
#out: The are 'single quotes' 

print ("The are \"double quotes\"")
#out: The are "double quotes" 

print ("The is a new line \nNew line")
#out: The is a new line
